    /**
     * 1: 1
     * 2: 2
     * 3: f(1) + f(2)
     * 4: f(2) + f(3)
     * n: f(n-2) + f(n-1)
     *
     * @param n
     * @return
     */
    public int climbStairs(int n) {
        if (n <= 2) return n;
        int f1 = 1;
        int f2 = 2;
        int f3 = 3;
        while ((n = n - 3) > 0) {
            f3 = f1 + f2;
            f1 = f2;
            f2 = f3;
        }
        return f3;
    }
